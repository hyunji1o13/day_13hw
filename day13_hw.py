import turtle as t
t.shape ("turtle")
t.color("white")#500 X 500 으로 범위 정하기
t.backward(500)
t.color("red")
t.pensize(5)#범위를 두껍게 그림
t.rt(90)
t.fd(250)
t.lt(90)
t.fd(500)
t.lt(90)
t.fd(500)
t.lt(90)
t.fd(500)
t.lt(90)
t.fd(500)
t.color("black") #상자와 구분하기 위해 색 변경
t.pensize(2)#굵기 차이를 위해 다시 변경
t.goto(1,40)
t.speed(0)
t.goto(-60,-250)
t.speed(2)
t.setheading(-120)#거북이 머리 각도를 옮김
t.goto(-90,250)
t.speed(5)
t.color("yellow")
t.goto(-250,-250)
t.speed(1)
t.color("black")
t.setheading(150)
t.goto(-500,200)
t.speed(5)
t.color("purple")
t.speed(1)
t.setheading(20)
t.goto(0,70)
t.color("black")
t.speed(0)
t.setheading(180)
t.goto(-500,0)
